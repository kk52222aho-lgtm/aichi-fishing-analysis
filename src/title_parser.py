"""釣り船ブログのタイトルから「狙い魚種」を抽出する。

釣り船ブログのタイトルは「X便行ってきました」「X釣りへ〜」のように
極めて定型的。YOLO のクラスに無い魚種（オニカサゴ等）も
タイトルからは取れるため、target_species の信頼源として有用。

注意:
  - 長い／より具体的なキーワードを先に試す（"オニカサゴ" を "カサゴ" より先に）
  - 部分文字列マッチなので「便」「釣り」「五目」等のサフィックスは不要
  - マッチしなかった場合は None（呼び出し側で dominant species 等にフォールバック）

例:
    extract_target_species("オニカサゴ便行ってきました")  -> "オニカサゴ"
    extract_target_species("アオリイカ釣行ってきました") -> "アオリイカ"
    extract_target_species("イサキ五目便")             -> "イサキ"  # 五目より先にイサキを拾う
    extract_target_species("予約状況です")             -> None
"""
from __future__ import annotations

from typing import Optional

# (キーワード, 出力する target_species)
# 長い／より具体的な名前を先に置く。最初にマッチしたものを返す。
_TARGET_KEYWORDS: tuple[tuple[str, str], ...] = (
    # 5 文字以上の複合名（短い名前を内包するので最優先）
    ("ガンゾウビラメ", "ガンゾウビラメ"),
    ("シューサイフグ", "シューサイフグ"),

    # 3-4 文字の複合名（短い名前と被るので優先）
    ("オニカサゴ",  "オニカサゴ"),
    ("アオリイカ",  "アオリイカ"),
    ("シマガツオ",  "シマガツオ"),
    ("シマアジ",    "シマアジ"),
    ("キンメダイ",  "キンメダイ"),
    ("アコウダイ",  "アコウダイ"),
    ("クロムツ",    "クロムツ"),
    ("シロギス",    "シロギス"),
    ("トラフグ",    "トラフグ"),     # "フグ" の前
    ("ホウボウ",    "ホウボウ"),

    # 2-3 文字の主要魚種
    ("マダイ",      "マダイ"),
    ("クロダイ",    "クロダイ"),
    ("チダイ",      "チダイ"),
    ("マダコ",      "マダコ"),
    ("カワハギ",    "カワハギ"),
    ("メバル",      "メバル"),
    ("カサゴ",      "カサゴ"),       # オニカサゴ の後
    ("イサキ",      "イサキ"),
    ("タチウオ",    "タチウオ"),
    ("ハマチ",      "ハマチ"),
    ("ワラサ",      "ワラサ"),
    ("ブリ",        "ブリ"),
    ("サワラ",      "サワラ"),
    ("カツオ",      "カツオ"),
    ("ヒラメ",      "ヒラメ"),
    ("マゴチ",      "マゴチ"),
    ("スズキ",      "スズキ"),
    ("セイゴ",      "セイゴ"),
    ("アジ",        "アジ"),         # シマアジ の後
    ("サバ",        "サバ"),
    ("フグ",        "フグ"),

    # 短い名前（被りやすいので最後）
    ("タコ",        "タコ"),         # マダコ の後
    ("イカ",        "イカ"),         # アオリイカ の後
    ("キス",        "キス"),         # シロギス の後

    # カテゴリ
    ("五目",        "五目"),
    ("根魚",        "根魚"),
)


def extract_target_species(title: Optional[str]) -> Optional[str]:
    """タイトル文字列から狙い魚種を1つ抽出。

    Args:
        title: ブログタイトル

    Returns:
        マッチした target_species。マッチしなければ None。
    """
    if not title:
        return None
    for keyword, target in _TARGET_KEYWORDS:
        if keyword in title:
            return target
    return None


def _cli() -> None:
    """簡易テスト用 CLI。タイトルを引数で渡すと target を表示。"""
    import sys
    for arg in sys.argv[1:]:
        print(f"{arg!r:50s} -> {extract_target_species(arg)}")


if __name__ == "__main__":
    _cli()
